// PRIMITIVOS: são conhecidos por serem imutáveis, o seu valor ao longo do tempo não muda
// os tipos primitivos podem ser transformados em objeto, quando encapsulados em classes especiais
// para conversão de dados, essas classes são chamadade de boxing ou wrapper (utilizado no java)

typeof 10; //number
typeof 'javascript'; //string
typeof true; //boolean
typeof Symbol(); //symbol (chegou no es6)
typeof null; //objeto
typeof undefined; //undefined


